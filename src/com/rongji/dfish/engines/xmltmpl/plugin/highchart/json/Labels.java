package com.rongji.dfish.engines.xmltmpl.plugin.highchart.json;

public class Labels {
	private String align;//right
	private Boolean enabled;
	private String format;
	private String formatter;
	private Integer maxStaggerLines;
	private String overflow;
	private Integer rotation;
	private Integer staggerLines;
	private Integer step;
	private CssObject style; 
	private Boolean useHTML;
	private Integer x;
	private Integer y;
	private Integer zIndex;
	
	public String getAlign() {
		return align;
	}
	public void setAlign(String align) {
		this.align = align;
	}
	public Boolean getEnabled() {
		return enabled;
	}
	public void setEnabled(Boolean enabled) {
		this.enabled = enabled;
	}
	public String getFormat() {
		return format;
	}
	public void setFormat(String format) {
		this.format = format;
	}
	public String getFormatter() {
		return formatter;
	}
	public void setFormatter(String formatter) {
		this.formatter = formatter;
	}
	public Integer getMaxStaggerLines() {
		return maxStaggerLines;
	}
	public void setMaxStaggerLines(Integer maxStaggerLines) {
		this.maxStaggerLines = maxStaggerLines;
	}
	public String getOverflow() {
		return overflow;
	}
	public void setOverflow(String overflow) {
		this.overflow = overflow;
	}
	public Integer getRotation() {
		return rotation;
	}
	public void setRotation(Integer rotation) {
		this.rotation = rotation;
	}
	public Integer getStaggerLines() {
		return staggerLines;
	}
	public void setStaggerLines(Integer staggerLines) {
		this.staggerLines = staggerLines;
	}
	public Integer getStep() {
		return step;
	}
	public void setStep(Integer step) {
		this.step = step;
	}
	public CssObject getStyle() {
		return style;
	}
	public void setStyle(CssObject style) {
		this.style = style;
	}
	public Boolean getUseHTML() {
		return useHTML;
	}
	public void setUseHTML(Boolean useHTML) {
		this.useHTML = useHTML;
	}
	public Integer getX() {
		return x;
	}
	public void setX(Integer x) {
		this.x = x;
	}
	public Integer getY() {
		return y;
	}
	public void setY(Integer y) {
		this.y = y;
	}
	public Integer getZIndex() {
		return zIndex;
	}
	public void setZIndex(Integer index) {
		zIndex = index;
	}

}
